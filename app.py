def page_downloads() -> None:
    """Download individual CSVs or full ZIP bundle."""
    st.markdown(
        """
<div class="hero">
  <div class="hero-title">📥 Downloads</div>
  <p class="hero-sub">Download individual tables or bundle everything into a single ZIP archive.</p>
</div>
""",
        unsafe_allow_html=True,
    )

    # ── Individual table downloads ────────────────────────────────────────────
    section_title("Synthetic Tables")
    for t in TABLES:
        icon  = TABLE_ICONS[t]
        df_s  = load_csv(synth_path(t))
        df_o  = load_csv(orig_path(t))
        bs    = read_bytes(synth_path(t))
        bo    = read_bytes(orig_path(t))

        with st.container():
            glass_card(
                f'<b style="color:#e2e8f0">{icon} {t.capitalize()}</b>'
                f'<span style="font-size:.75rem;color:#475569;margin-left:.8rem">'
                f'{f"{len(df_s):,} synthetic rows · {len(df_s.columns)} cols" if df_s is not None else "Synthetic: not generated"}'
                f' &nbsp;|&nbsp; '
                f'{f"{len(df_o):,} original rows" if df_o is not None else "Original: not found"}'
                f'</span>'
            )
            c1, c2, _ = st.columns([1, 1, 3])
            with c1:
                if bs:
                    download_button(bs, f"synthetic_{t}.csv", f"⬇ Synthetic {t.capitalize()}")
                else:
                    st.caption("Synthetic CSV not found")
            with c2:
                if bo:
                    download_button(bo, f"{t}_ready.csv", f"⬇ Original {t.capitalize()}")
                else:
                    st.caption("Original CSV not found")

    # ── Evaluation downloads ──────────────────────────────────────────────────
    section_title("Evaluation Outputs")
    ev_files = [
        ("evaluation_summary.csv",  "⬇ Scores CSV",       "text/csv"),
        ("evaluation_summary.json", "⬇ Full JSON",         "application/json"),
        ("report.html",             "⬇ HTML Report",       "text/html"),
    ]
    ecols = st.columns(len(ev_files))
    for col, (fname, label, mime) in zip(ecols, ev_files):
        b = read_bytes(str(PROJECT_ROOT / "outputs" / "evaluation" / fname))
        with col:
            if b:
                download_button(b, fname, label, mime=mime)
            else:
                st.caption(f"{fname} not found")

    # ── ZIP bundle ────────────────────────────────────────────────────────────
    section_title("Full ZIP Bundle")
    zc1, zc2 = st.columns([1, 2])

    with zc1:
        if st.button("📦 Build ZIP Archive", key="zip_build"):
            with st.spinner("Building archive…"):
                buf = BytesIO()
                with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
                    for t in TABLES:
                        for src, dest in [
                            (synth_path(t), f"synthetic/synthetic_{t}.csv"),
                            (orig_path(t),  f"original/{t}_ready.csv"),
                        ]:
                            if Path(src).exists():
                                zf.write(src, dest)
                    ev_dir = PROJECT_ROOT / "outputs" / "evaluation"
                    for fname in ["evaluation_summary.csv", "evaluation_summary.json", "report.html"]:
                        p = ev_dir / fname
                        if p.exists():
                            zf.write(str(p), f"evaluation/{fname}")
                buf.seek(0)
                st.download_button(
                    "⬇ Download ZIP",
                    data=buf.read(),
                    file_name=f"synthfhir_dataset_{datetime.now().strftime('%Y%m%d_%H%M%S')}.zip",
                    mime="application/zip",
                    key="zip_dl",
                )

    with zc2:
        glass_card(
            """
<div style="font-size:.82rem;color:#94a3b8">The ZIP archive contains:</div>
<ul style="font-size:.78rem;color:#64748b;margin:.5rem 0 0;padding-left:1.1rem;line-height:2">
  <li><code>synthetic/*.csv</code> — Generated synthetic tables</li>
  <li><code>original/*.csv</code> — Preprocessed originals</li>
  <li><code>evaluation/evaluation_summary.csv</code></li>
  <li><code>evaluation/evaluation_summary.json</code></li>
  <li><code>evaluation/report.html</code></li>
</ul>
"""
        )

    render_footer()


