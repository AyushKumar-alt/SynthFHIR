def theme(fig: go.Figure) -> go.Figure:
    """Apply dark theme to any Plotly figure."""
    fig.update_layout(**_LAYOUT)
    return fig


def plot_histogram_compare(
    real: pd.Series,
    synth: pd.Series,
    col: str,
    bins: int = 40,
) -> go.Figure:
    """Overlay histogram comparing original vs synthetic for a numeric column."""
    fig = go.Figure()
    fig.add_trace(go.Histogram(
        x=real, name="Original", nbinsx=bins,
        histnorm="probability density", opacity=0.62,
        marker_color="#38bdf8", hovertemplate="%{x}<br>density=%{y:.4f}<extra>Original</extra>",
    ))
    fig.add_trace(go.Histogram(
        x=synth, name="Synthetic", nbinsx=bins,
        histnorm="probability density", opacity=0.62,
        marker_color="#f472b6", hovertemplate="%{x}<br>density=%{y:.4f}<extra>Synthetic</extra>",
    ))
    fig.update_layout(barmode="overlay", title=f"Distribution — {col}",
                      legend=dict(orientation="h", y=1.08))
    return theme(fig)


def plot_category_compare(
    real: pd.Series,
    synth: pd.Series,
    col: str,
    top_n: int = 15,
) -> go.Figure:
    """Grouped bar chart comparing original vs synthetic category frequencies."""
    r = real.value_counts(normalize=True).head(top_n)
    s = synth.value_counts(normalize=True).head(top_n)
    cats = sorted(set(r.index) | set(s.index))
    fig = go.Figure()
    fig.add_trace(go.Bar(x=cats, y=[r.get(c, 0) for c in cats], name="Original",
                         marker_color="#38bdf8", hovertemplate="%{x}: %{y:.3f}<extra>Original</extra>"))
    fig.add_trace(go.Bar(x=cats, y=[s.get(c, 0) for c in cats], name="Synthetic",
                         marker_color="#f472b6", hovertemplate="%{x}: %{y:.3f}<extra>Synthetic</extra>"))
    fig.update_layout(barmode="group", title=f"Category Frequencies — {col}",
                      legend=dict(orientation="h", y=1.08))
    return theme(fig)


def plot_missing_values(df_orig: pd.DataFrame, df_synth: pd.DataFrame) -> go.Figure:
    """Bar chart of missing-value percentages for shared columns."""
    shared = [c for c in df_orig.columns if c in df_synth.columns]
    orig_m  = [round(df_orig[c].isna().mean() * 100, 2) for c in shared]
    synth_m = [round(df_synth[c].isna().mean() * 100, 2) for c in shared]
    df = pd.DataFrame({"Column": shared, "Original": orig_m, "Synthetic": synth_m})
    df = df[(df["Original"] > 0) | (df["Synthetic"] > 0)]
    if df.empty:
        fig = go.Figure()
        fig.add_annotation(text="No missing values in either dataset", showarrow=False,
                           font=dict(color="#64748b", size=14))
        return theme(fig)
    fig = go.Figure()
    fig.add_trace(go.Bar(x=df["Column"], y=df["Original"],  name="Original",  marker_color="#38bdf8"))
    fig.add_trace(go.Bar(x=df["Column"], y=df["Synthetic"], name="Synthetic", marker_color="#f472b6"))
    fig.update_layout(barmode="group", title="Missing Values (%)", yaxis_title="%",
                      legend=dict(orientation="h", y=1.08))
    return theme(fig)


def plot_heatmap(matrix: pd.DataFrame, title: str) -> go.Figure:
    """Plotly imshow heatmap."""
    fig = px.imshow(
        matrix.round(3),
        text_auto=".2f",
        color_continuous_scale=["#0c1a2e", "#0ea5e9", "#e2e8f0"],
        title=title,
        aspect="auto",
    )
    fig.update_traces(hovertemplate="%{x} × %{y}: %{z:.3f}<extra></extra>")
    return theme(fig)


