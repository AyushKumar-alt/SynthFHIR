def _col_of(df: pd.DataFrame, *keywords: str) -> Optional[str]:
    """Find first column whose name contains any keyword (case-insensitive)."""
    for kw in keywords:
        for c in df.columns:
            if kw.lower() in c.lower():
                return c
    return None


def _numeric_col_selector(df_o: pd.DataFrame, df_s: pd.DataFrame, key: str) -> Optional[str]:
    num = [c for c in df_o.columns if c in df_s.columns
           and pd.api.types.is_numeric_dtype(df_o[c])
           and not pd.api.types.is_bool_dtype(df_o[c])
           and df_o[c].nunique() > 2]
    return st.selectbox("Numeric column", num, key=key) if num else None


def _cat_col_selector(df_o: pd.DataFrame, df_s: pd.DataFrame, key: str) -> Optional[str]:
    cats = [c for c in df_o.columns if c in df_s.columns
            and (df_o[c].dtype == object or pd.api.types.is_bool_dtype(df_o[c]))
            and df_o[c].nunique() <= 40]
    return st.selectbox("Categorical column", cats, key=key) if cats else None


