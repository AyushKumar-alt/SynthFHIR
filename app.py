def _table_specific_charts(table: str, df_o: pd.DataFrame, df_s: pd.DataFrame) -> None:
    """Render extra visualizations specific to each table."""
    section_title("Table-Specific Analysis")

    if table == "patients":
        cols = st.columns(3)
